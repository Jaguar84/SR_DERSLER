package TAPSIRIQLAR;

public class OneMonthQuestion1 {
    public static void main(String[] args) {
        System.out.println(method2(200,20,150));
    }

    private static int method2(int a, int b, int c){
        int max=0;
        if(a>max){
            max=a;
        }
        if(b>max){
            max=b;
        }
        if(c>max){
            max=c;
        }
        return max;
    }
}
